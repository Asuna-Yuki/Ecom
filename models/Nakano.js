const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const nakanoSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
    default: 0,
  },
  description: {
    type: String,
    required: true,
  },
  quantity: {
    type: Number,
    required: true,
    default: 0,
  },
  reviews: [],
});

module.exports = Nakano = mongoose.model("nakano", nakanoSchema);
